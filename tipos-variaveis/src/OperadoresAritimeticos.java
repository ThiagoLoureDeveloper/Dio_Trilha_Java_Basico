public class OperadoresAritimeticos {
    
    public static void main(String[] args) {
        String nomeCompleto = "Thiago" + " Loure";
        System.out.println(nomeCompleto);

        String concatenacao = "?";

        System.out.println(concatenacao);
        
        concatenacao = 1 + 1 + "1";
        System.out.println(concatenacao);
        
        concatenacao = 1 + "1" + 1 + "1";
        System.out.println(concatenacao);
        
        concatenacao = "1" + 1 + 1 + 1;
        System.out.println(concatenacao);
        
        concatenacao = "1" + (1 + 1 + 1);
        System.out.println(concatenacao);
        

    }
    
}
